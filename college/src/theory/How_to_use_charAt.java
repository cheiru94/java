package theory;

public class How_to_use_charAt {

	public static void main(String[] args) {
		String msg = "betty";

		char word0 = msg.charAt(0);
		char word1 = msg.charAt(1);
		char word2 = msg.charAt(2);
		char word3 = msg.charAt(3);
		char word4 = msg.charAt(4);

		System.out.println(word0);
		System.out.println(word1);
		System.out.println(word2);
		System.out.println(word3);
		System.out.println(word4);

		
		
		
	}

}
